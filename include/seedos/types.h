/*
 * Common type definitions
 *
 * Aggregates standard types for kernel use:
 *   - Integer types (uint8_t, int64_t, etc.) from <stdint.h>
 *   - Size types (size_t, NULL) from <stddef.h>
 *   - Boolean types (bool, true, false) from <stdbool.h>
 */

#ifndef _TYPES_H
#define _TYPES_H

#include <stdint.h>
#include <stddef.h>
#include <stdbool.h>

#endif /* _TYPES_H */